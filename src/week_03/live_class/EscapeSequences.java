package week_03.live_class;

public class EscapeSequences {
    public static void main (String[] args){
        System.out.println("how are you my pretty love?");
        System.out.println("how are you my pretty \tlove?");
        System.out.println("how are you my pretty \blove?");
        System.out.println("how are you my pretty \nlove?");
        System.out.println("how are you my pretty \rlove?");

    }
}
