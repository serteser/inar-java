package week_01.assignments;

public class Question_01_09 {
    public static void main(String[] args) {
        /*
        (Area and perimeter of a rectangle) The program which is below displays the area and perimeter of a rectangle
        with the width of 4.5 and height of 7.9 using the following Formula:

                                            area= width * height
                                            perimeter= 2 * (width + height)
        */
        System.out.println("Area of the rectangle:");
        System.out.println(4.5 * 7.9);
        System.out.println("Perimeter of the rectangle:");
        System.out.println(2 * (4.5 + 7.9));
    }
}
